package pe.gob.trabajo.pcd.vista.bean;

public class ReporteEmpresa {

	private Long id;
	private String razonSocial;
	private String actEconomica;
	private Long cantTrabajadores;

	public ReporteEmpresa() {
		super();
		id = 0L;
		razonSocial = "";
		actEconomica = "";
		cantTrabajadores = 0L;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getRazonSocial() {
		return razonSocial;
	}

	public void setRazonSocial(String razonSocial) {
		this.razonSocial = razonSocial;
	}

	public String getActEconomica() {
		return actEconomica;
	}

	public void setActEconomica(String actEconomica) {
		this.actEconomica = actEconomica;
	}

	public Long getCantTrabajadores() {
		return cantTrabajadores;
	}

	public void setCantTrabajadores(Long cantTrabajadores) {
		this.cantTrabajadores = cantTrabajadores;
	}

}
