package pe.gob.trabajo.pcd.modelo.dominio;

public class GrupoOcupacion {

	private static final long serialVersionUID = 1088108989769380409L;

	private String id;
	private String descripcion;
	private String abrggocup;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	public String getAbrggocup() {
		return abrggocup;
	}

	public void setAbrggocup(String abrggocup) {
		this.abrggocup = abrggocup;
	}
}
