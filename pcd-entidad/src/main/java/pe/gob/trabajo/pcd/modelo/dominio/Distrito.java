package pe.gob.trabajo.pcd.modelo.dominio;

public class Distrito {

	private static final long serialVersionUID = 1088108989769380409L;

	private DistritoId id;
	private Provincia provincia;
	private String nombre;
	private String abreviatura;
	private String codZona;
	private String codRegion;
	private String codDepReniec;
	private String codProvReniec;
	private String codDisReniec;

	public DistritoId getId() {
		return id;
	}

	public void setId(DistritoId id) {
		this.id = id;
	}

	public Provincia getProvincia() {
		return provincia;
	}

	public void setProvincia(Provincia provincia) {
		this.provincia = provincia;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getAbreviatura() {
		return abreviatura;
	}

	public void setAbreviatura(String abreviatura) {
		this.abreviatura = abreviatura;
	}

	public String getCodZona() {
		return codZona;
	}

	public void setCodZona(String codZona) {
		this.codZona = codZona;
	}

	public String getCodRegion() {
		return codRegion;
	}

	public void setCodRegion(String codRegion) {
		this.codRegion = codRegion;
	}

	public String getCodDepReniec() {
		return codDepReniec;
	}

	public void setCodDepReniec(String codDepReniec) {
		this.codDepReniec = codDepReniec;
	}

	public String getCodProvReniec() {
		return codProvReniec;
	}

	public void setCodProvReniec(String codProvReniec) {
		this.codProvReniec = codProvReniec;
	}

	public String getCodDisReniec() {
		return codDisReniec;
	}

	public void setCodDisReniec(String codDisReniec) {
		this.codDisReniec = codDisReniec;
	}

}
