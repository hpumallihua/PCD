package pe.gob.trabajo.pcd.modelo.dominio;

public class Menu {

	private static final long serialVersionUID = 1088108989769380409L;

	private MenuId id;
	private Sistema sistema;
	private Integer nivel;
	private String codMenuPadre;
	private String descripcion;
	private String url;
	private String estado;
	private Integer orden;
	private String icono;

	public MenuId getId() {
		return id;
	}

	public void setId(MenuId id) {
		this.id = id;
	}

	public Sistema getSistema() {
		return sistema;
	}

	public void setSistema(Sistema sistema) {
		this.sistema = sistema;
	}

	public Integer getNivel() {
		return nivel;
	}

	public void setNivel(Integer nivel) {
		this.nivel = nivel;
	}

	public String getCodMenuPadre() {
		return codMenuPadre;
	}

	public void setCodMenuPadre(String codMenuPadre) {
		this.codMenuPadre = codMenuPadre;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getEstado() {
		return estado;
	}

	public void setEstado(String estado) {
		this.estado = estado;
	}

	public Integer getOrden() {
		return orden;
	}

	public void setOrden(Integer orden) {
		this.orden = orden;
	}

	public String getIcono() {
		return icono;
	}

	public void setIcono(String icono) {
		this.icono = icono;
	}

}
